uint64_t args[128];

uint64_t xorshift64()
{
	static uint64_t x64 = 88172645463325252ull;
	x64 ^= x64 << 13;
	x64 ^= x64 >> 7;
	x64 ^= x64 << 17;
	return x64;
}

void init_args()
{
	int argp = 0;

	args[argp++] = 0;
	args[argp++] = 1;
	args[argp++] = -1;
	args[argp++] = 1LL << 63;

	int i = 2 + (xorshift64() & 7);
	while (i < 63) {
		assert(argp < 128);
		args[argp++] = 1LL << i;
		i += 1 + (xorshift64() & 7);
	}

	for (int i = 4; i < 64; i += 4) {
		assert(argp < 127);
		args[argp++] = xorshift64() & ((1LL << i)-1);
		args[argp++] = xorshift64() & _rv64_grev((1LL << i)-1, -1);
	}

	while (argp < 128) {
		args[argp++] = xorshift64();
	}
}

